using System;
using System.Collections.Generic;
using TaleWorlds.ObjectSystem;

namespace TaleWorlds.CampaignSystem
{
	public class CampaignObjectBase : MBObjectBase
	{
		internal static void AutoGeneratedStaticCollectObjectsCampaignObjectBase(object o, List<object> collectedObjects)
		{
			((CampaignObjectBase)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
		}

		protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
		{
			base.AutoGeneratedInstanceCollectObjects(collectedObjects);
		}
	}
}
