using System;
using System.Collections.Generic;
using TaleWorlds.CampaignSystem.CampaignBehaviors;
using TaleWorlds.Core;
using TaleWorlds.Localization;
using TaleWorlds.SaveSystem;

namespace TaleWorlds.CampaignSystem.MapNotificationTypes
{
	public class EducationMapNotification : InformationData
	{
		internal static void AutoGeneratedStaticCollectObjectsEducationMapNotification(object o, List<object> collectedObjects)
		{
			((EducationMapNotification)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
		}

		protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
		{
			base.AutoGeneratedInstanceCollectObjects(collectedObjects);
			collectedObjects.Add(this.Child);
		}

		internal static object AutoGeneratedGetMemberValueChild(object o)
		{
			return ((EducationMapNotification)o).Child;
		}

		internal static object AutoGeneratedGetMemberValueAge(object o)
		{
			return ((EducationMapNotification)o).Age;
		}

		public override TextObject TitleText
		{
			get
			{
				return new TextObject("{=Vv2ednFa}Education", null);
			}
		}

		public override string SoundEventPath
		{
			get
			{
				return "event:/ui/notification/child_born";
			}
		}

		[SaveableProperty(1)]
		public Hero Child { get; private set; }

		[SaveableProperty(11)]
		public int Age { get; private set; }

		public EducationMapNotification(Hero child, int age, TextObject descriptionText)
			: base(descriptionText)
		{
			this.Child = child;
			this.Age = age;
		}

		public override bool IsValid()
		{
			return Campaign.Current.GetCampaignBehavior<IEducationLogic>().IsValidEducationNotification(this);
		}
	}
}
