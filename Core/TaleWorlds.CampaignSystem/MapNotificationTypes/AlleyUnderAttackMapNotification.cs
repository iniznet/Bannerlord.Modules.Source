		{
			base.AutoGeneratedInstanceCollectObjects(collectedObjects);
			collectedObjects.Add(this.Alley);
		}

		internal static object AutoGeneratedGetMemberValueAlley(object o)
		{
			return ((AlleyUnderAttackMapNotification)o).Alley;
		}

