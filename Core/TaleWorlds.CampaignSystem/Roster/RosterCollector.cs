using System;
using System.Collections.Generic;
using TaleWorlds.SaveSystem;

namespace TaleWorlds.CampaignSystem.Roster
{
	public class RosterCollector
	{
		internal static void AutoGeneratedStaticCollectObjectsRosterCollector(object o, List<object> collectedObjects)
		{
			((RosterCollector)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
		}

		protected virtual void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
		{
			collectedObjects.Add(this.MemberRoster);
			collectedObjects.Add(this.PrisonRoster);
			collectedObjects.Add(this.ItemRoster);
		}

		internal static object AutoGeneratedGetMemberValueMemberRoster(object o)
		{
			return ((RosterCollector)o).MemberRoster;
		}

		internal static object AutoGeneratedGetMemberValuePrisonRoster(object o)
		{
			return ((RosterCollector)o).PrisonRoster;
		}

		internal static object AutoGeneratedGetMemberValueItemRoster(object o)
		{
			return ((RosterCollector)o).ItemRoster;
		}

		public RosterCollector()
		{
			this.MemberRoster = TroopRoster.CreateDummyTroopRoster();
			this.PrisonRoster = TroopRoster.CreateDummyTroopRoster();
			this.ItemRoster = new ItemRoster();
		}

		[SaveableField(0)]
		public TroopRoster MemberRoster;

		[SaveableField(1)]
		public TroopRoster PrisonRoster;

		[SaveableField(2)]
		public ItemRoster ItemRoster;
	}
}
