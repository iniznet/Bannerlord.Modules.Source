using System;
using System.Collections.Generic;
using TaleWorlds.CampaignSystem.Party;

namespace TaleWorlds.CampaignSystem.MapEvents
{
	public class FieldBattleEventComponent : MapEventComponent
	{
		internal static void AutoGeneratedStaticCollectObjectsFieldBattleEventComponent(object o, List<object> collectedObjects)
		{
			((FieldBattleEventComponent)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
		}

		protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
		{
			base.AutoGeneratedInstanceCollectObjects(collectedObjects);
		}

		protected FieldBattleEventComponent(MapEvent mapEvent)
			: base(mapEvent)
		{
		}

		public static FieldBattleEventComponent CreateFieldBattleEvent(PartyBase attackerParty, PartyBase defenderParty)
		{
			MapEvent mapEvent = new MapEvent();
			FieldBattleEventComponent fieldBattleEventComponent = new FieldBattleEventComponent(mapEvent);
			mapEvent.Initialize(attackerParty, defenderParty, fieldBattleEventComponent, MapEvent.BattleTypes.FieldBattle);
			Campaign.Current.MapEventManager.OnMapEventCreated(mapEvent);
			return fieldBattleEventComponent;
		}

		public static FieldBattleEventComponent CreateComponentForOldSaves(MapEvent mapEvent)
		{
			return new FieldBattleEventComponent(mapEvent);
		}

		protected override void OnInitialize()
		{
		}

		protected override void OnFinalize()
		{
		}
	}
}
