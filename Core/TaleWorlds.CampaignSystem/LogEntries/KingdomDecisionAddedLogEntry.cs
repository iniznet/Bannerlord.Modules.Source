using System;
using System.Collections.Generic;
using TaleWorlds.CampaignSystem.Election;

namespace TaleWorlds.CampaignSystem.LogEntries
{
	public class KingdomDecisionAddedLogEntry : LogEntry
	{
		internal static void AutoGeneratedStaticCollectObjectsKingdomDecisionAddedLogEntry(object o, List<object> collectedObjects)
		{
			((KingdomDecisionAddedLogEntry)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
		}

		protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
		{
			base.AutoGeneratedInstanceCollectObjects(collectedObjects);
		}

		public override CampaignTime KeepInHistoryTime
		{
			get
			{
				return CampaignTime.Weeks(1f);
			}
		}

		public KingdomDecisionAddedLogEntry(KingdomDecision decision, bool isPlayerInvolved)
		{
		}

		public override string ToString()
		{
			return "Kingdom Decision Log added";
		}
	}
}
