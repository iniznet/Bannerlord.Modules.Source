using System;
using TaleWorlds.SaveSystem.Definition;

namespace TaleWorlds.Localization
{
	internal class AutoGeneratedSaveManager : IAutoGeneratedSaveManager
	{
		public void Initialize(DefinitionContext definitionContext)
		{
			TypeDefinition typeDefinition = (TypeDefinition)definitionContext.TryGetTypeDefinition(new TypeSaveId(20001));
			CollectObjectsDelegate collectObjectsDelegate = new CollectObjectsDelegate(TextObject.AutoGeneratedStaticCollectObjectsTextObject);
			typeDefinition.InitializeForAutoGeneration(collectObjectsDelegate);
			typeDefinition.GetPropertyDefinitionWithId(new MemberTypeId(2, 2)).InitializeForAutoGeneration(new GetPropertyValueDelegate(TextObject.AutoGeneratedGetMemberValueAttributes));
			typeDefinition.GetFieldDefinitionWithId(new MemberTypeId(2, 1)).InitializeForAutoGeneration(new GetFieldValueDelegate(TextObject.AutoGeneratedGetMemberValueValue));
		}
	}
}
