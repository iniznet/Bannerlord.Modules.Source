using System;
using TaleWorlds.SaveSystem.Definition;

namespace TaleWorlds.ObjectSystem
{
	internal class AutoGeneratedSaveManager : IAutoGeneratedSaveManager
	{
		public void Initialize(DefinitionContext definitionContext)
		{
			TypeDefinition typeDefinition = (TypeDefinition)definitionContext.TryGetTypeDefinition(new TypeSaveId(10034));
			CollectObjectsDelegate collectObjectsDelegate = new CollectObjectsDelegate(MBObjectBase.AutoGeneratedStaticCollectObjectsMBObjectBase);
			typeDefinition.InitializeForAutoGeneration(collectObjectsDelegate);
			typeDefinition.GetPropertyDefinitionWithId(new MemberTypeId(2, 1)).InitializeForAutoGeneration(new GetPropertyValueDelegate(MBObjectBase.AutoGeneratedGetMemberValueStringId));
			typeDefinition.GetPropertyDefinitionWithId(new MemberTypeId(2, 2)).InitializeForAutoGeneration(new GetPropertyValueDelegate(MBObjectBase.AutoGeneratedGetMemberValueId));
			typeDefinition.GetPropertyDefinitionWithId(new MemberTypeId(2, 3)).InitializeForAutoGeneration(new GetPropertyValueDelegate(MBObjectBase.AutoGeneratedGetMemberValueIsRegistered));
		}
	}
}
