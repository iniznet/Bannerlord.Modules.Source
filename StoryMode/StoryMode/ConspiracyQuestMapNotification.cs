using System;
using System.Collections.Generic;
using TaleWorlds.CampaignSystem;
using TaleWorlds.Core;
using TaleWorlds.Localization;
using TaleWorlds.SaveSystem;

namespace StoryMode
{
	public class ConspiracyQuestMapNotification : InformationData
	{
		public override TextObject TitleText
		{
			get
			{
				return new TextObject("{=f3NvtzpE}Conspiracy Quest Available", null);
			}
		}

		public override string SoundEventPath
		{
			get
			{
				return "";
			}
		}

		[SaveableProperty(1)]
		public QuestBase ConspiracyQuest { get; private set; }

		public ConspiracyQuestMapNotification(QuestBase conspiracyQuest, TextObject descriptionText)
			: base(descriptionText)
		{
			this.ConspiracyQuest = conspiracyQuest;
		}

		internal static void AutoGeneratedStaticCollectObjectsConspiracyQuestMapNotification(object o, List<object> collectedObjects)
		{
			((ConspiracyQuestMapNotification)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
		}

		protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
		{
			base.AutoGeneratedInstanceCollectObjects(collectedObjects);
			collectedObjects.Add(this.ConspiracyQuest);
		}

		internal static object AutoGeneratedGetMemberValueConspiracyQuest(object o)
		{
			return ((ConspiracyQuestMapNotification)o).ConspiracyQuest;
		}
	}
}
